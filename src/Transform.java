public class Transform {
    private float x;
    private float y;
    private float rotation;
    private float scaleX;
    private float scaleY;

    public void Transform(float x, float y) {
        this.x = x;
        this.y = y;
        this.rotation = 0;
        this.scaleX = 1;
        this.scaleY = 1;
    }
}
